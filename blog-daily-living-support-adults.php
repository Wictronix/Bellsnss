                                <p>Maybe you need a hand with morning routines so you can get to work on time. Perhaps meal preparation feels overwhelming with your current abilities. Or maybe household tasks have become too physically demanding. Whatever your situation, the right support can transform daily challenges into manageable routines.</p>

                                <h3>What Daily Living Support Actually Means</h3>

                                <p>Daily living support for adults with disability encompasses all the assistance you might need to manage everyday tasks and live as independently as possible. This includes personal care assistance with showering, dressing, grooming, and toileting, meal planning, preparation, and assistance with eating if needed, household tasks like cleaning, laundry, and light maintenance, medication reminders and management, and support to develop daily living skills that increase your independence over time.</p>
                                
                                <p>The goal isn't just to get tasks done, it's to help you participate actively in your own life. At Bells NSS, we call it "active support" because our workers don't just do things for you; they work alongside you, encouraging and enabling you to do as much as you comfortably can.</p>

