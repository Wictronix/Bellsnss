                                <h3>What Travel Assistance Covers</h3>

                                <p>NDIS travel assistance for social activities helps you get to and from community participation activities when mainstream transport options don't work for you. This support can include a support worker accompanying you on public transport if you need supervision or assistance, transport provided directly by your support worker in their vehicle, booking and funding accessible taxi or rideshare services for approved activities, or assistance to learn how to use public transport independently over time.</p>
                                
                                <p>The key is that the travel must be directly related to your NDIS goals around social and community participation. At Bells NSS, we help participants understand what qualifies and how to include this crucial support in their NDIS plans.</p>

