                                    <h3>What NDIS Community Nursing Does NOT Cover</h3>
                                    <p>The NDIS won't fund services that should be provided by the mainstream health system, including hospital-in-the-home programs for acute illness, short-term post-surgical nursing (typically covered for first 2 weeks), emergency medical care, GP consultations, or medications and medical supplies.</p>
                                    <p>Also excluded are health needs not related to your disability, nursing care that informal supports (family) can reasonably provide, and services already funded under aged care packages if you're receiving both.</p>
