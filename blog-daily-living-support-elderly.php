                                <p>Daily living support for elderly people on the NDIS looks different from support for younger participants. Your parent might be managing multiple age-related conditions alongside their disability, perhaps Parkinson's disease with dementia, diabetes with reduced mobility, or stroke recovery complicated by arthritis. These overlapping conditions require specialized understanding and flexible support.</p>
                                
