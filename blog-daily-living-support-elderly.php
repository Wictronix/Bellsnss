                                    <h3>The Intersection of NDIS and Aged Care</h3>
                                    <p>Here's where things can get confusing. Some elderly people qualify for both NDIS and aged care supports. Daily living support for elderly participants must come from one source or the other, not both for the same service. Generally, if your parent's needs are primarily disability-related and they became a participant before turning 65, they continue with NDIS. If needs are primarily age-related, aged care may be more appropriate.</p>
                                    <p>At Bells NSS, we help families navigate this complexity. We work with NDIS and aged care systems to ensure your parent receives all the support they're entitled to, from the right funding source.</p>
